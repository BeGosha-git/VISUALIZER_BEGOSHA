def tr(key: str, **kwargs: Any) -> str:
    """Перевод по ключу. Язык читается из настроек при каждом вызове."""
    try:
        from config.app_settings import settings

        lang = settings.language()
    except Exception:
        lang = "ru"
    table = _EN if lang == "en" else _RU
    s = table.get(key)
    if s is None:
        s = _RU.get(key, key)
    if kwargs:
        try:
            return s.format(**kwargs)
        except Exception:
            return s
    return s


def movement_label(internal: str) -> str:
    return tr(
        {
            "vertical": "move.vertical",
            "horizontal": "move.horizontal",
            "scale": "move.scale",
            "rotate": "move.rotate",
        }.get(internal, "move.vertical")
    )


